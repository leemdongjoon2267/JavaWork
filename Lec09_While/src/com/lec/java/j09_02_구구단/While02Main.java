package com.lec.java.j09_02_구구단;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");

		int i = 1;
		while(i < 10){
			System.out.println("2 x " + i + " = " + (2 * i));
			i++;
		}

		System.out.println("\n프로그램 종료");
	} // end main()

} // end class While02Main









